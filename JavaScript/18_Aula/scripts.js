/**
 * Operadores lógicos
 * && -> AND -> E
 * || -> OR -> OU
 * ! -> NOT -> NÃO
 */

//AND
console.log(true && true);
console.log(true && false);
console.log(false && false);

//OR
console.log(true || true);
console.log(true || false);
console.log(false || false);

//NOT
console.log(!true);
console.log(!false);